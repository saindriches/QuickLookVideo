    // Reconstruct palette from stsd (VerbatimSampleDescription) for PAL8 codecs.
    // Copied from ff_get_qtpalette().
    class func makeQuickTimePalette(formatDescription: CMVideoFormatDescription) -> Data? {
        guard
            let stsd = formatDescription.extensions[kCMFormatDescriptionExtension_VerbatimSampleDescription as CFString] as? Data
        else {
            return nil
        }
        guard stsd.count >= 86 else { return nil }

        // Helpers for handling unaligned access
        func be16(_ offset: Int) -> UInt16 {
            let hi = UInt16(stsd[offset])
            let lo = UInt16(stsd[offset + 1])
            return (hi << 8) | lo
        }
        func be32(_ offset: Int) -> UInt32 {
            let b0 = UInt32(stsd[offset])
            let b1 = UInt32(stsd[offset + 1])
            let b2 = UInt32(stsd[offset + 2])
            let b3 = UInt32(stsd[offset + 3])
            return (b0 << 24) | (b1 << 16) | (b2 << 8) | b3
        }

        let depthFlags = Int(be16(82))
        let bitDepth = depthFlags & 0x1F
        let greyscale = (depthFlags & 0x20) != 0
        let colorTableID = Int16(bitPattern: be16(84))

        guard bitDepth == 1 || bitDepth == 2 || bitDepth == 4 || bitDepth == 8 else {
            return nil
        }

        var palette = [UInt32](repeating: 0xFF00_0000, count: 256)
        let colorCount = 1 << bitDepth

        if greyscale && bitDepth > 1 && colorTableID != 0 {
            var colorIndex = 255
            let colorDec = 256 / (colorCount - 1)
            for i in 0..<colorCount {
                let c = UInt32(max(0, colorIndex) & 0xFF)
                palette[i] = 0xFF00_0000 | (c << 16) | (c << 8) | c
                colorIndex = max(0, colorIndex - colorDec)
            }
        } else if colorTableID != 0 {
            // Default Macintosh palette requested by non-zero color table ID.
            let defaultPalette: [UInt8]
            switch bitDepth {
            case 1:
                defaultPalette = qtDefaultPalette2
            case 2:
                defaultPalette = qtDefaultPalette4
            case 4:
                defaultPalette = qtDefaultPalette16
            default:
                defaultPalette = qtDefaultPalette256
            }
            for i in 0..<colorCount {
                let r = UInt32(defaultPalette[i * 3 + 0])
                let g = UInt32(defaultPalette[i * 3 + 1])
                let b = UInt32(defaultPalette[i * 3 + 2])
                palette[i] = 0xFF00_0000 | (r << 16) | (g << 8) | b
            }
        } else {
            guard stsd.count >= 94 else { return nil }
            let colorStart = Int(be32(86))
            let colorEnd = Int(be16(92))
            if colorStart < 0 || colorEnd < colorStart || colorEnd > 255 { return nil }

            var off = 94
            for i in colorStart...colorEnd {
                guard off + 8 <= stsd.count else { return nil }
                let r = UInt32(stsd[off + 2])
                let g = UInt32(stsd[off + 4])
                let b = UInt32(stsd[off + 6])
                palette[i] = 0xFF00_0000 | (r << 16) | (g << 8) | b
                off += 8
            }
        }

        var out = Data(count: Int(AVPALETTE_SIZE))
        out.withUnsafeMutableBytes { raw in
            let p = raw.baseAddress!.assumingMemoryBound(to: UInt32.self)
            for i in 0..<256 { p[i] = palette[i] }
        }
        return out
    }

